function isEven(number){
    if (number % 2 === 0){
        return true;
    }

    else{
        return false;
    }
}


console.log(isEven(89))             //Ans:  false
console.log(isEven(7644))           //Ans:  true 





function oddNumber(num){
    if (num % 2 === 1){
        return true;
    }

    return false;
}


console.log (oddNumber(9))                  //Ans: true
console.log (oddNumber(26))                 //Ans: false